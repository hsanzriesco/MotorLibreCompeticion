// Esperar a que el DOM cargue
document.addEventListener("DOMContentLoaded", () => {
  const form = document.getElementById("registerForm");
  const message = document.getElementById("message");

  // Escuchar el evento de env√≠o del formulario
  form.addEventListener("submit", async (e) => {
    e.preventDefault();

    // Obtener los valores de los inputs
    const name = document.getElementById("name").value.trim();
    const email = document.getElementById("email").value.trim();
    const password = document.getElementById("password").value.trim();

    // Verificaci√≥n r√°pida
    if (!name || !email || !password) {
      message.textContent = "‚ö†Ô∏è Todos los campos son obligatorios.";
      message.classList.add("text-danger");
      return;
    }

    console.log("üì§ Enviando datos:", { name, email, password });

    try {
      const response = await fetch("/api/register", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
        },
        body: JSON.stringify({ name, email, password }),
      });

      const data = await response.json();
      console.log("üì• Respuesta del servidor:", data);

      if (response.ok) {
        // Registro exitoso
        message.textContent = "‚úÖ Registro exitoso. Bienvenido a Motor Libre Competici√≥n.";
        message.classList.remove("text-danger");
        message.classList.add("text-success");

        // Limpiar el formulario
        form.reset();
      } else {
        // Error controlado desde el backend
        message.textContent = "‚ùå " + (data.error || "Error en el registro.");
        message.classList.remove("text-success");
        message.classList.add("text-danger");
      }
    } catch (error) {
      // Error en la conexi√≥n o servidor
      console.error("‚ùå Error en el fetch:", error);
      message.textContent = "‚ùå Error de conexi√≥n con el servidor.";
      message.classList.remove("text-success");
      message.classList.add("text-danger");
    }
  });
});
